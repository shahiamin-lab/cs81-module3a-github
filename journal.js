// Greet a user by name
function greet(name) {
    return "Hello, " + name + "!";
}

// Check if a number is even
function isEven(num) {
    return num % 2 === 0;
}
// Add two numbers
function add(a, b) {
    return a + b;
}

// Convert miles to kilometers
function milesToKm(miles) {
    return miles * 1.60934;
}
